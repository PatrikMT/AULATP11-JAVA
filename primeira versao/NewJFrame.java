    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText().isEmpty() || jTextField5.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty() ){
            JOptionPane.showMessageDialog(NewJFrame.this,"Por favor, preencha os campos obrigat√≥rios");
        } else if(isValidInteger(jTextField1.getText()) && isValidInteger(jTextField5.getText()) ) {
            umlivro.setCodigo(jTextField1.getText());
            umlivro.setAno(jTextField5.getText());
            umlivro.setTitulo(jTextField2.getText());
            umlivro.setAutor(jTextField3.getText()); 
            umlivro.setEditora(jTextField4.getText());
        }else {JOptionPane.showMessageDialog(NewJFrame.this,"O campo c√≥digo e o campo ano devem ser numeros inteiros");}
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

